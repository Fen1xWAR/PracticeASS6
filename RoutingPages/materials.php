                        <p>Важно подчеркнуть, что часто в литературе указывают только «принципы фон Неймана», но это несправедливо. Фундаментальные идеи независимо друг от друга сформулировали американский математик и физик Джон фон Нейман и советский инженер, учёный Сергей Алексеевич Лебедев. Главный конструктор первой отечественной вычислительной машины МЭСМ С. А. Лебедев не только автор проектов компьютеров серии БЭСМ (Большая Электронная Счётная Машина) и принципиальных положений компьютера «Эльбрус», но и человек удивительной скромности с активной жизненной позицией.</p>

