                        <p>Во время демонстрации функциональной схемы (слайд 6) анимация подчёркивает направление движения потоков обмена информацией между устройствами. Ответ на вопрос на слайде 8: при отключении источника энергии вся информация, содержащаяся в оперативной памяти (ОЗУ), пропадёт. ОЗУ — энергозависимая память.</p>

                        <p>Во время демонстрации слайдов, посвящённых составу компонентов (слайд 9, 10), следует обратить внимание учащихся на то, что вводимая и выводимая информация обязательно проходит «через» алгоритмы обработки информации.</p>

                        <p>На слайде 11 «Принцип двоичного кодирования» имеется возможность перехода (кнопка «лупа») на скрытый слайд (слайд 12) с исторической справкой о троичном компьютере «Сетунь». В нём была применена уравновешенная троичная система счисления, использование которой впервые в истории позволило представлять одинаково просто как положительные, так и отрицательные числа, что при двоичном кодировании невозможно.</p>

                        <p>На слайде 14, посвящённом принципу адресности памяти, приведён пример адреса, записанного в 16-ричной системе счисления — 25F0:A3ED. Первая часть 25F0 — адрес сегмента, вторая A3ED — смещение внутри сегмента. Под запись адреса ячейки здесь отведено 4 байта. В 16-ричной системе счисления существует 16 цифр. 16 ≤ 24. 4 бита используется для записи одной цифры. 8 цифр * 4 бита = 32 бита = 4 байта.</p>

                        <p>Оценим максимально возможный объём памяти компьютера, допускающего такую адресацию. Каждому байту памяти соответствует свой уникальный адрес. На запись одного адреса отведено 32 бита. Разных адресов существует 2^32. 2^32 байт = 2^22 Гбайт = 4 Гбайта.</p>

                        <p>Слайд 20 содержит интерактивные элементы: «Шина адреса», «Шина данных», «Шина управления», «К» (контроллер). При выборе триггера открывается соответствующее пояснение.</p>

                        <p>Ответ на вопрос 4 на слайде 25: для записи цифры в 16-ричной системе счисления необходимо 4 бита. Адрес — три цифры в 16-ричной системе счисления (одна — для записи адреса сегмента, три — для записи смещения). 4 * 4 = 16 бит отведено под запись адреса каждого байта. Разных адресов в этом случае получается 2^16 байт = 2^6 Кбайт = 64 Кбайта.</p>

